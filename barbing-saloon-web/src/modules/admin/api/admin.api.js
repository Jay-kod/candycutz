import client from '../../../core/api/axios';

export const adminApi = {
  dashboard: () => client.get('/admin/dashboard'),
  appointments: () => client.get('/admin/appointments'),
  approveAppointment: (id) => client.patch(`/admin/appointments/${id}/approve`),
  cancelAppointment: (id) => client.patch(`/admin/appointments/${id}/cancel`),
  createWalkIn: (data) => client.post('/admin/appointments/walk-in', data),
  services: () => client.get('/admin/services'),
  createService: (data) => client.post('/admin/services', data),
  updateService: (id, data) => client.post(`/admin/services/${id}`, data),
  deleteService: (id) => client.delete(`/admin/services/${id}`),
  serviceCategories: () => client.get('/admin/service-categories'),
  gallery: () => client.get('/admin/gallery'),
  addGalleryImage: (data) => client.post('/admin/gallery', data, { headers: { 'Content-Type': 'multipart/form-data' } }),
  updateGalleryImage: (id, data) => client.post(`/admin/gallery/${id}`, data, { headers: { 'Content-Type': 'multipart/form-data' } }),
  deleteGalleryImage: (id) => client.delete(`/admin/gallery/${id}`),
  testimonials: () => client.get('/admin/testimonials'),
  approveTestimonial: (id) => client.put(`/admin/testimonials/${id}`, { is_approved: true }),
  featureTestimonial: (id) => client.patch(`/admin/testimonials/${id}/feature`),
  deleteTestimonial: (id) => client.delete(`/admin/testimonials/${id}`),
  blogPosts: () => client.get('/admin/blog'),
  createBlogPost: (data) => client.post('/admin/blog', data),
  updateBlogPost: (id, data) => client.post(`/admin/blog/${id}`, data),
  deleteBlogPost: (id) => client.delete(`/admin/blog/${id}`),
  workingHours: () => client.get('/admin/working-hours'),
  reports: () => client.get('/admin/reports'),
  settings: () => client.get('/admin/settings'),
  updateSettings: (data) => client.post('/admin/settings', data),
  barbers: () => client.get('/admin/barbers'),
  createBarber: (data) => client.post('/admin/barbers', data),
  updateBarber: (id, data) => client.put(`/admin/barbers/${id}`, data),
  updateBarberStatus: (id, status) => client.patch(`/admin/barbers/${id}/status`, { status }),
  deleteBarber: (id) => client.delete(`/admin/barbers/${id}`),
  logs: () => client.get('/admin/logs'),
  customers: () => client.get('/admin/customers'),
  analytics: (range) => client.get('/admin/analytics', { params: { range } }),
  verifications: (params) => client.get('/admin/verifications', { params }),
  verifyAppointment: (id) => client.patch(`/admin/verifications/${id}/verify`),
  verificationStats: () => client.get('/admin/verifications/stats'),
};